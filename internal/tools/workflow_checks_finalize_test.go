// writeRecipeFiles generates all recipe files in a temp directory using templates + comments.
func writeRecipeFiles(t *testing.T, dir string, plan *workflow.RecipePlan) {
	t.Helper()

	files := workflow.BuildFinalizeOutput(plan)
	// Replace the TODO-scaffold app README with a real-looking one so the
	// scaffold-TODO check passes (the check exists to force agents to overlay
	// the real README from the mount; tests don't have a mount).
	files["appdev/README.md"] = validTestAppREADME()
	for relPath, content := range files {
		fullPath := filepath.Join(dir, relPath)
		if err := os.MkdirAll(filepath.Dir(fullPath), 0o755); err != nil {
			t.Fatal(err)
		}
		// Add comments to import.yaml to meet 30% ratio.
		if filepath.Base(relPath) == "import.yaml" {
			content = addCommentsToYAML(content)
		}
		if err := os.WriteFile(fullPath, []byte(content), 0o644); err != nil {
			t.Fatal(err)
		}
	}
}


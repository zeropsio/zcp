// addCommentsToYAML adds inline comments to YAML content to meet comment ratio.
func addCommentsToYAML(content string) string {
	// Prepend comment block and add per-line comments to meet 30% ratio.
	var result []string
	result = append(result,
		"# Recipe import configuration",
		"# Generated by ZCP recipe workflow",
		"# This file defines the project and services",
		"# for this environment tier",
		"# See the environment README.md for details",
		"# Import this file to create your Zerops project",
	)
	for line := range strings.SplitSeq(content, "\n") {
		result = append(result, line)
		trimmed := strings.TrimSpace(line)
		// Add a comment after every substantive line to ensure ratio.
		if trimmed != "" && !strings.HasPrefix(trimmed, "#") && strings.Contains(trimmed, ":") {
			result = append(result, "  # "+trimmed)
		}
	}
	return strings.Join(result, "\n")
}

